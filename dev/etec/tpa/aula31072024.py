# iteração de lista
fruta = ['banana', 'maça', 'laranja', 'mexerica', 'pera']

for fruta in fruta:
    print(fruta)

# Iteração com texto
for caractere in 'ABDIAS':
    print(caractere)

# Usando com range
for i in range (5):
    print(1)

# Abrangência de Laço
estados = ['Acre', 'Bahia', 'Espirito Santo', 'Parana', 'Santa Catarina']

for estado in estados:
    print(estado)
print('Acabou o For')

